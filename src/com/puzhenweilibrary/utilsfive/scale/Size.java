public class Size {
    
    private int mWidth;
    private int mHeight;
    
    public Size(final int width, final int height) {
        mWidth = width;
        mHeight = height;
    }
    
    public int getWidth() {
        return mWidth;
    }
    
    public int getHeight() {
        return mHeight;
    }
}